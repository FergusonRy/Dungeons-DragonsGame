
public class Clas
	{
		private String name;
		private int hitDie;
		
		
		public Clas (String n, int hd)
		{
			name = n;
			hitDie = hd;
		}

		public String getName()
			{
				return name;
			}

		public void setName(String name)
			{
				this.name = name;
			}

		public int getHitDie()
			{
				return hitDie;
			}

		public void setHitDie(int hitDie)
			{
				this.hitDie = hitDie;
			}

	}
